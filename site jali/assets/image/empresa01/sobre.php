<h2>Nascimento da Marca
<br><br>
A história teve início há mais de um século, na Alemanha, e traduz o primeiro capítulo da motorização veicular no mundo. Os protagonistas Gottlieb Daimler e Carl Benz construíram paralelamente os primeiros automóveis motorizados do mundo.
<br><br>
O pioneirismo desses homens fez com que colecionassem outras conquistas como a construção do primeiro ônibus, do primeiro caminhão com motor a gasolina e do primeiro caminhão a diesel do mundo. Em abril de 1900, Emil Jellinek e a DMG assinaram um acordo de distribuição de veículos e motores. Logo de início, foi acertado o desenvolvimento de uma nova forma de motor que deveria levar o nome "Daimler-Mercedes".
<br><br>
Poucos dias mais tarde, Jellinek encomendava 36 veículos pelo preço global de 550 mil marcos, que, em valores atuais, corresponderia a 5,5 milhões de marcos. Não apenas para aquela época, foi um grande pedido. Depois de mais algumas semanas, ele encomendou mais 36 veículos, todos com motor de 8 HP.
<br><br>
O primeiro dos veículos com o novo motor, um carro de competição com 36 HP, foi entregue em dezembro de 1900 a Jellinek.
<br><br>
Este primeiro "Mercedes", desenvolvido por Wilhelm Maybach, construtor chefe da DMG, faz furor no início do século. As características do veículo - entre elas, seus baixos pontos de apoio, quadro de aço prensado, motor leve de alto desempenho e radiador tipo colmeia - representavam uma riqueza de inovações que o tornaram o primeiro automóvel moderno.
<br><br>
A "Semana de Nice", em março de 1901, com a Mercedes praticamente imbatível em todos os requisitos, ajudou Jellinek e a Mercedes com uma publicidade excepcional.
<br><br>
Em março de 1901 foram lançados os modelos irmãos 12/16 HP e 8/11 HP. As encomendas de Jellinek excederam a capacidade de produção da fábrica Daimler de Cannstatt.
<br><br>
A Estrela
<br><br>
O mundialmente famoso símbolo da Mercedes-Benz teve um início profético. Representando a triplicidade das atividades da Daimler, fabricante de motores para uso em terra, mar e ar, a estrela de três pontas foi adotada como logotipo em 1909, após a morte de Gottlieb Daimler.
<br><br>
Foi inspirada numa figura que ele havia desenhado num postal, o qual remeteu à sua esposa com o seguinte comentário: um dia essa estrela brilhará sobre a minha obra.
<br><br>
 Ao longo dos anos, o símbolo passou por várias alterações. Em 1923 foi acrescentado o círculo. E três anos depois, com a fusão das empresas Daimler e Benz, foi incluída a coroa de louros, do logotipo da Benz. A forma definitiva foi adotada em 1933 e desde então se mantém inalterada.</h2>
